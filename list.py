# Define two lists
list1 = [1, 2, 3, 4, 5]
list2 = [6, 7, 8, 9, 10]

# Addition
addition_result = [a + b for a, b in zip(list1, list2)]
print("Addition Result:", addition_result)

# Subtraction
subtraction_result = [a - b for a, b in zip(list1, list2)]
print("Subtraction Result:", subtraction_result)

# Multiplication
multiplication_result = [a * b for a, b in zip(list1, list2)]
print("Multiplication Result:", multiplication_result)



